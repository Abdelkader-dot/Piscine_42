#include <stdio.h>
#include <string.h>

int	ft_strlen(char *str)
{
	int	i;
	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

int	main(void)
{
	char	c[] = "12345678";

	printf("%d", ft_strlen(c));
	return (0);
}
